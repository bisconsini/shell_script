#!/usr/bin/env bash

#[----Cria swap-------------------------------------------------------------------------------]
sudo swapon -s
sudo fallocate -l 2G /swap
sudo chmod 600 /swap
sudo mkswap /swap
#[--------------------------------------------------------------------------------------------]

#[----Adiciona swap ao fstab------------------------------------------------------------------]
sudo echo "
# swap
/swap   none    swap    sw    0   0" >> /etc/fstab

sudo nano /etc/fstab
#[--------------------------------------------------------------------------------------------]
